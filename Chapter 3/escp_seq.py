print("Hello\nWorld")   #newline

print("Hello\tWorld")    #Horizontal tab

print("This is a backslash: \\")     #backslash

print('It\'s a good day')     #single quote

print("He said, \"Hello!\"")    #double quote

print("Hello\rWorld")     #Carriage return

print("Hello\bWorld")     #Backspace

print("Hello\fWorld")     #Form feed

print("Alert!\a")    # Bell/Alert (produces a beep sound)

print("Hello\vWorld")     #Vertical Tab